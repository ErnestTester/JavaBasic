public class IntegerTestExercise
{
    public static void main(String[] args)
    {
        //Use the int keyword to declare a integer variable
        int carSpeed;
        carSpeed=60;
        System.out.println("The actual speed for the car is:" + carSpeed);
        //Car speed was increased with 20, 60 + 20 = 80;
        carSpeed+=20;
        System.out.println("Now the speed is:" + carSpeed);
    }
}
